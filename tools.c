#include "wolf.h"

int			test_line(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (!is_white(str[i]) && str[i] != '-' && !is_num(str[i]))
			return (1);
		i++;
	}
	return (0);
}

int			is_num(char c)
{
	return (c == '0' || c == '1' || c == '2' || c == '4');
}
